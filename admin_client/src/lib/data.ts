// Helper function to get owner by ID
export function getOwnerById(ownerId: string): Owner | null {
  return owners.find((owner) => owner.ownerId === ownerId) || null
}

// Helper function to get facilities by owner ID
export function getFacilitiesByOwnerId(ownerId: string): Facility[] {
  return facilities.filter((facility) => facility.ownerId === ownerId)
}

// Format currency to VND format
export function formatCurrency(amount: number): string {
  return new Intl.NumberFormat("vi-VN", {
    style: "currency",
    currency: "VND",
    minimumFractionDigits: 0,
    maximumFractionDigits: 0,
  }).format(amount)
}

// Format date to be more readable
export function formatDate(dateString: string): string {
  const date = new Date(dateString)
  return date.toLocaleDateString("en-US", {
    year: "numeric",
    month: "short",
    day: "numeric",
  })
}


